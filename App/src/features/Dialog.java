    private void tampilDataTable(ModelDetailTransaksi modelDetail) {
        txtNoTransaksi.setText(modelDetail.getModelTransaksi().getNoTransaksi());
        txtIdCustomer.setText(modelDetail.getModelTransaksi().getModelCustomer().getIdCustomer());
        txtNamaCustomer.setText(modelDetail.getModelTransaksi().getModelCustomer().getNamaCustomer());
        txtIdPelayan.setText(modelDetail.getModelTransaksi().getModelKaryawan().getIdKaryawan());
        txtNamaPelayan.setText(modelDetail.getModelTransaksi().getModelKaryawan().getNamaKaryawan());
        txtIdUser.setText(modelDetail.getModelTransaksi().getModelUser().getIdUser());
        DecimalFormat df = new DecimalFormat("#,##0.##");
        txtTotal.setText(df.format(modelDetail.getModelTransaksi().getTotal()));
        txtBayar.setText(df.format(modelDetail.getModelTransaksi().getBayar()));
        txtKembali.setText(df.format(modelDetail.getModelTransaksi().getKembali()));
        txtJenisPembayaran.setText(modelDetail.getModelTransaksi().getJenisPembayaran());
        
        
        String query = "SELECT dtl.No_Transaksi, dtl.ID_Layanan, lyn.ID_Jenis_Layanan, jns.Nama_Layanan, \n" +
        "lyn.Tipe_Layanan, lyn.Harga, dtl.Diskon, dtl.Subtotal_Transaksi, dtl.Kode_Voucher FROM detail_transaksi dtl \n" +
        "JOIN layanan lyn ON dtl.ID_Layanan=lyn.ID_Layanan JOIN jenis_layanan jns \n" +
        "ON lyn.ID_Jenis_Layanan=jns.ID_Jenis_Layanan WHERE No_Transaksi=?";
        
        try {
            PreparedStatement pst = connection.prepareStatement(query);
            pst.setString(1, modelDetail.getModelTransaksi().getNoTransaksi());
            ResultSet rst = pst.executeQuery();
            while(rst.next()) {
                String idLayanan = rst.getString("ID_Layanan");
                String namaLayanan = rst.getString("Nama_Layanan")
                        .concat(" - ")
                        .concat(rst.getString("Tipe_Layanan"));
                double harga = rst.getDouble("Harga");
                double diskon = rst.getDouble("Diskon");
                double subtotal = rst.getDouble("Subtotal_Transaksi");
                String kodeVoucher = rst.getString("Kode_Voucher");
                tabmodel.addRow(new Object[]{idLayanan, namaLayanan, harga, diskon, kodeVoucher, subtotal});
            }
        } catch(Exception ex) {
            ex.printStackTrace();
        }
    }
    
    //  Style Table
    private void styleTable(JScrollPane scroll, JTable table, int columnTable) {
        scroll.getViewport().setBackground(new Color(255,255,255));
        JPanel panel = new JPanel();
        panel.setBackground(new Color(255,255,255));
        scroll.setCorner(JScrollPane.UPPER_RIGHT_CORNER, panel);
        scroll.setBorder(new EmptyBorder(5,10,5,10));
        table.setRowHeight(35);        
        table.getTableHeader().setDefaultRenderer(new ModelHeaderTable());
        table.setDefaultRenderer(Object.class, new ModelRenderTable(columnTable));
    }
    
